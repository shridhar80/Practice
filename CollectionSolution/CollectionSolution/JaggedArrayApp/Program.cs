int[][] jaggedArray = new int[3][];
jaggedArray[0] = new int[2] { 1, 2 };
jaggedArray[1] = new int[3] { 3, 4, 5 };
jaggedArray[2] = new int[1] { 6 };

/*
 * 
 A jagged array (also known as an array of arrays) is an array whose 
elements are arrays themselves.
Each element (subarray) can have different lengths.


Characteristics:

Each subarray can have a different length.
Memory is allocated in a more fragmented way compared to rectangular arrays.
Provides flexibility in handling arrays where the size of each row may vary.


 */



/*
 
Rectangular Array: Storing and manipulating pixel values in an image where 
each pixel has a fixed number of color channels.

Jagged Array: Storing student scores in different subjects 
where each student may have a different number of subjects or 
different assignments per subject.
 */ 